def get_name_month(month):
  if month == 1:
    return 'Enero'
  elif month == 2:
    return 'Febrero'
  elif month == 3:
    return 'Marzo'
  elif month == 4:
    return 'Abril'
  elif month == 5:
    return 'Mayo'
  elif month == 6:
    return 'Junio'
  elif month == 7:
    return 'Julio'
  elif month == 8:
    return 'Agosto'
  elif month == 9:
    return 'Septiembre'
  elif month == 10:
    return 'Octubre'
  elif month == 11:
    return 'Noviembre'
  elif month == 12:
    return 'Diciembre'
